# Import the math module
# Import allows Python to import other modules or programs into other programs
import math

my_number = 81
my_string_number = "81"
my_float = 3.8
my_factorial = 4

# Square root
print (math.sqrt(my_number))

# Convert string to number
print (my_string_number * 4)

# Ceiling
print (math.ceil(my_float))

# factorial num!
print (math.factorial(my_factorial))

# Floor
print (math.floor(my_float))



