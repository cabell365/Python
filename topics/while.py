# Default values
# Python variables store values in memory
x = 0
max = 10

# print while x is less than max
# we increment x by 1 each iteration
while x < max:
    print(x)
    x+=1

print()

# print while i not equal 0
# Decrement i by 1 each iteration
i = 10
not_equal_value = 0
while i != not_equal_value:
    print(i)
    i-=1