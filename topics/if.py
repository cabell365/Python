# Define variable
# Python variables store values in memory
mynum = 20
mymax = 50
mymin = 10

# If value is equal
if mynum == 20:
    print("my num is %s\n" % mynum)

# If not equal
if mynum != 20:
    print ("Not my num\n")

# Less than
if mynum < mymax:
    print("my num is %s\n" % mynum)

# Greater than
if mynum > mymin:
    print("my num is %s\n" % mynum)

# If / else with not equal
if mynum != 20:
    print ("Not my num\n")
else:
    print("my num is %s\n" % mynum)
    


